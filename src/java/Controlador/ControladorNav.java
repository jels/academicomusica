    public String getNav(String user) {
        String htmlcode = "";
        Usuario us = new Usuario();
        us.setNombreUsuario(user);
        Rol_model rm = new Rol_model();
        switch (rm.findRol(us)) {
            case "SuperAdministrador":
                htmlcode += getViewNavRoot();
                break;
            case "Director":
                htmlcode += getViewNavDire();
                break;
            case "Docente":
                htmlcode += getViewNavDoce();
                break;
            case "Secretari@":
                htmlcode += getViewNavSecre();
                break;
            case "Estudiante":
                htmlcode += getViewNavEst();
                break;
            case "false":
                htmlcode += "";
                break;
            default:
                break;
        }
        return htmlcode;
    }

